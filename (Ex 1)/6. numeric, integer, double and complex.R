a=10
if(class(a)=="numeric"){
  print("Numeric = TRUE")
}else{
  print("Numeric = FALSE")
}
if(class(a)=="character"){
  print("Character = TRUE")
}else{
  print("Character = FALSE")
}
if(class(a)=="logical"){
  print("Logical = TRUE")
}else{
  print("Logical = FALSE")
}
if(class(a)=="complex"){
  print("Complex = TRUE")
}else{
  print("Complex = FALSE")
}